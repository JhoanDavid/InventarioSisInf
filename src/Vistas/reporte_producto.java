    public void calcularCostotoal() {
        double costototal = 0.0;
        double ventatotal = 0.0;

        for (Producto obj : listaProducto) {
            costototal += (obj.getCantidadStock() * obj.getValorCompra());
            ventatotal += (obj.getCantidadStock() * obj.getValorVenta());
        }
        double gananciatotal = (ventatotal - costototal);
        txtcosto.setText(String.valueOf(costototal));
        txtventa.setText(String.valueOf(ventatotal));
        txtganancia.setText(String.valueOf(gananciatotal));
        txtcosto.setEditable(false);
        txtventa.setEditable(false);
        txtganancia.setEditable(false);
    }

    public void LlenarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) tablaAgregarproducto.getModel();
        listaProducto = controlproducto.findProductoEntities();
        for (Producto obj : listaProducto) {
            modelo.addRow(new Object[]{obj.getId(), obj.getDescripcion(), obj.getUnidadMedida(), obj.getValorCompra(), obj.getValorVenta(), obj.getGanancia(), obj.getCantidadStock(), obj.getEstado()});

        }
    }

