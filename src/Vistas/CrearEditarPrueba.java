    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
     PruebaController pc =new PruebaController(); 
     String password=new String(txtPass.getPassword());
     String password2=new String(txtPass2.getPassword());
     
     if(password.equals(password2)){
        if(pc.CrearPrueba(txtNombre.getText(), txtEmail.getText(), password, "Activo")){
         JOptionPane.showMessageDialog(null,"Prueba creada satisfactoriamente"); 
         Inicio i=new Inicio();
         i.setVisible(true); //pone la nueva ventana visible
         this.dispose();
        }else{
         JOptionPane.showMessageDialog(null,"no se pudo crear la prueba");
        }
     }else{
         JOptionPane.showMessageDialog(null,"Las contrase침as ingresadas no coinciden");
     }
   
     
    }//GEN-LAST:event_btnCrearActionPerformed

